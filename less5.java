class less5 {

    public static void main(String args[]) {

        int x = 5;


        switch (x) {

            case 1:
                System.out.println("чмсло рівне 1");
                break;

            case 5:
                System.out.println("чмсло рівне 5");
                x++;
                break;

            case 6:
                System.out.println("чмсло рівне 6");
                break;

            case 9:
                System.out.println("чмсло рівне 9");
                break;
            default:
                System.out.println("чмсло не рівне 1,5,6,9");
        }

    }
}